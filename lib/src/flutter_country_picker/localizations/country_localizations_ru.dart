  @override
  String getContinentName(dynamic continent) {
    if (continent == null) return 'Все';

    switch (continent.toString()) {
      case 'Continent.europe':
        return 'Европа';
      case 'Continent.asia':
        return 'Азия';
      case 'Continent.africa':
        return 'Африка';
      case 'Continent.northAmerica':
        return 'Северная Америка';
      case 'Continent.southAmerica':
        return 'Южная Америка';
      case 'Continent.oceania':
        return 'Океания';
      case 'Continent.antarctica':
        return 'Антарктида';
      default:
        return continent.toString();
    }
  }

